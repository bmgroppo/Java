package Exercicio1;

public class Genero {

    private int codigo;
    private String descricao;

    public void setCod(int cod) {
        this.codigo = cod;
    }

    public void setDesc(String desc) {
        this.descricao = desc;
    }

    public int getCod() {
        return this.codigo;
    }

    public String getDesc() {
        return this.descricao;
    }
}
